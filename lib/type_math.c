#include "type_math.h"

double type_log(double exp) {
    return log(exp);
}

double type_log10(double exp) {
    return log10(exp);
}

/* Unused */
float type_log_float(float exp) {
    return logf(exp);
}

double type_pow(double base, double exponent) {
    return pow(base, exponent);
}

double type_sqrt(double sq) {
    return sqrt(sq);
}